
let num1 = parseInt(prompt('Enter First Number: '))
let operator = prompt('Enter Operator From the List (+, -, *, /): ')
let num2 = parseInt(prompt('Enter Second Number: '))

if(operator == '+')
{
    alert(num1 + num2)
}


else if(operator == '-')
{
    alert(num1 - num2)
}


else if(operator == '*')
{
    alert(num1 * num2)
}


else if(operator == '/')
{
    alert(num1 / num2)
}